void make_csv_map(char* file_name, int** map) {
    FILE *csv_file;
    csv_file = fopen(file_name, "w+");
    for (int i = 0; i < mapHEIGHT; i++) {
        for (int j = 0; j < mapWIDTH; j++) {
            fprintf(csv_file, "%d", world_map[i][j]);
            if (j != mapWIDTH - 1) fprintf(csv_file, ",");
            else fprintf(csv_file, "\n");    
        }
    }
    fclose(csv_file);
}